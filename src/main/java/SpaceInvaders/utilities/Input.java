package SpaceInvaders.utilities;

/**
 * Enumerator For all the Possible Inputs.
 */
public enum Input {
    /**
     * Left direction = negative x-axis.
     */
    LEFT,

    /**
     * Right direction = positive x-axis.
     */
    RIGHT,

    /**
     * The Shoot action.
     */
    SHOOT,

    /**
     * Idle.
     */
    NONE,
}
